def _svg_icon(svg_str, size=22, color="#333333"):
    """Render an SVG string to a Retina-ready QIcon via QSvgRenderer."""
    colored = svg_str.replace('<svg ', f'<svg fill="{color}" ', 1)
    renderer = QSvgRenderer(QByteArray(colored.encode()))
    dpr = 2  # render at 2× for Retina
    pix = QPixmap(size * dpr, size * dpr)
    pix.fill(Qt.GlobalColor.transparent)
    p = QPainter(pix)
    renderer.render(p)
    p.end()
    pix.setDevicePixelRatio(dpr)
    return QIcon(pix)


def _make(draw_fn, size=22):
    """Create a QIcon from a QPainter draw function (for dynamic/custom icons)."""
    pix = QPixmap(size, size)
    pix.fill(Qt.GlobalColor.transparent)
    p = QPainter(pix)
    p.setRenderHint(QPainter.RenderHint.Antialiasing)
    draw_fn(p, size)
    p.end()
    return QIcon(pix)


def _pen(p, color="#333333", width=1.5):
    pen = QPen(QColor(color))
    pen.setWidthF(width)
    pen.setCapStyle(Qt.PenCapStyle.RoundCap)
    pen.setJoinStyle(Qt.PenJoinStyle.RoundJoin)
    p.setPen(pen)
    p.setBrush(Qt.BrushStyle.NoBrush)


